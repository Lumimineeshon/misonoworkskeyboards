void encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) {
        if (clockwise) {
            tap_code(KC_VOLU);
        } else {
            tap_code(KC_VOLD);
        }
    }
    else if (index == 1) {
        if (clockwise) {
            tap_code(KC_VOLU);
        } else {
            tap_code(KC_VOLD);
        }
    }
    else if (index == 2) {
        if (clockwise) {
            tap_code(KC_MRWD);
        } else {
            tap_code(KC_MFFD);
			}
		}
    else if (index == 3) {
        if (clockwise) {
            tap_code(KC_WBAK);
        } else {
            tap_code(KC_WFWD);
			}
		}
}

